//? 1. Extraction of Digits Algorithm:
// Step 1: Modulo(%) divide the number with 10
// Step 2: Modulo division will extract the remainder from ending
// Step 3: The loop will run till the number becomes 0.

//* Example: Given a Number 7789, extract all the digits
// 7789 % 10 => 778.9 = 9 (Remainder )
// 778  % 10 => 77.8  = 8 (Remainder)
// 77   % 10 => 7.7   = 7 (Remainder)
// 7    % 10 => 7     = 7 (Remainder)